const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const outputTs = path.join(root, 'src', 'generated', 'appShellVersion.ts');
const outputJson = path.join(root, 'public', 'app-shell-version.json');

const includedRoots = ['package.json', 'public', 'src'];
const excludedPathParts = [
  path.join('public', 'app-shell-version.json'),
  path.join('src', 'data', 'questions'),
  path.join('src', 'generated', 'appShellVersion.ts'),
];

const includedExtensions = new Set([
  '.css',
  '.html',
  '.ico',
  '.js',
  '.json',
  '.png',
  '.svg',
  '.ts',
  '.tsx',
  '.webmanifest',
]);

function normalize(filePath) {
  return path.relative(root, filePath).split(path.sep).join('/');
}

function isExcluded(filePath) {
  const relative = path.relative(root, filePath);
  return excludedPathParts.some(
    (excluded) => relative === excluded || relative.startsWith(`${excluded}${path.sep}`)
  );
}

function collectFiles(entry) {
  const fullPath = path.join(root, entry);
  if (!fs.existsSync(fullPath) || isExcluded(fullPath)) {
    return [];
  }

  const stat = fs.statSync(fullPath);
  if (stat.isFile()) {
    return includedExtensions.has(path.extname(fullPath)) ? [fullPath] : [];
  }

  return fs
    .readdirSync(fullPath)
    .flatMap((child) => collectFiles(path.join(entry, child)));
}

const files = includedRoots.flatMap(collectFiles).sort((a, b) => normalize(a).localeCompare(normalize(b)));
const hash = crypto.createHash('sha256');

for (const file of files) {
  hash.update(normalize(file));
  hash.update('\0');
  hash.update(fs.readFileSync(file));
  hash.update('\0');
}

const version = hash.digest('hex').slice(0, 16);

fs.mkdirSync(path.dirname(outputTs), { recursive: true });
fs.writeFileSync(
  outputTs,
  `// Generated by scripts/generate-app-shell-version.js before production builds.\nexport const APP_SHELL_VERSION = '${version}';\n`,
  'utf8'
);

fs.writeFileSync(
  outputJson,
  `${JSON.stringify({ version }, null, 2)}\n`,
  'utf8'
);

console.log(`Generated app shell version ${version} from ${files.length} files.`);
